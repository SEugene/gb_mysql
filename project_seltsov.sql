DROP DATABASE IF EXISTS projectshop;
CREATE DATABASE projectshop;
USE projectshop;

DROP TABLE IF EXISTS users;
CREATE TABLE users (
	id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY, 
    firstname VARCHAR(50),
    lastname VARCHAR(50),
    email VARCHAR(120) UNIQUE,
 	password_hash VARCHAR(100),
	phone BIGINT UNSIGNED UNIQUE,
	loyality_id BIGINT UNSIGNED,
	
	FOREIGN KEY (loyality_id) REFERENCES loyality(id),
	
	
    INDEX users_firstname_lastname_idx(firstname, lastname));

DROP TABLE IF EXISTS profiles;
CREATE TABLE profiles (
	user_id BIGINT UNSIGNED,
    gender CHAR(10),
    birthday DATE,
    created_at DATETIME DEFAULT NOW(),
    address_id BIGINT UNSIGNED NOT NULL UNIQUE,
	
    FOREIGN KEY (address_id) REFERENCES address(id),
    FOREIGN KEY (user_id) REFERENCES users(id));

DROP TABLE IF EXISTS address;
CREATE TABLE address (
	id SERIAL PRIMARY KEY,
	zip_code BIGINT,
	city VARCHAR(50),
	street VARCHAR(150),
	building VARCHAR(10),
	appartment VARCHAR(10));

DROP TABLE IF EXISTS product;
CREATE TABLE product (
	id SERIAL PRIMARY KEY,
	author VARCHAR(50),
	album VARCHAR(255),
	album_year INT,
	media_id BIGINT UNSIGNED,
	genre_id BIGINT UNSIGNED,
	price DECIMAL (11,2),
	
	FOREIGN KEY (genre_id) REFERENCES genre(id),
	FOREIGN KEY (media_id) REFERENCES media(id),

	INDEX products_author_idx(author));

DROP TABLE IF EXISTS media;
CREATE TABLE media (
	id SERIAL,
	media_type VARCHAR(10),
	box_type VARCHAR(255),
	description VARCHAR(255));

DROP TABLE IF EXISTS genre;
CREATE TABLE genre (
	id SERIAL PRIMARY KEY,
	genre VARCHAR(100));

DROP TABLE IF EXISTS orders;
CREATE TABLE orders (
	id SERIAL PRIMARY KEY,
	user_id BIGINT UNSIGNED,
	created_at DATETIME DEFAULT NOW(),
	updated_at DATETIME DEFAULT NOW(),

	FOREIGN KEY (user_id) REFERENCES users(id));

DROP TABLE IF EXISTS orders_content;
CREATE TABLE orders_content (
	order_id BIGINT UNSIGNED,
	product_id BIGINT UNSIGNED,
	product_quantity INT,
	total_sum DECIMAL (11,2),

	FOREIGN KEY (order_id) REFERENCES orders (id),
	FOREIGN KEY (product_id) REFERENCES product(id));

DROP TABLE IF EXISTS warehouses;	
CREATE TABLE warehouses (
	id SERIAL PRIMARY KEY,
	city VARCHAR(50),
	street VARCHAR(150),
	building VARCHAR(10));

DROP TABLE IF EXISTS discounts;
CREATE TABLE discounts (
	id SERIAL PRIMARY KEY,
	product_id BIGINT UNSIGNED,
	discount FLOAT UNSIGNED,
	started_at DATETIME,
    finished_at DATETIME,
   
    FOREIGN KEY (product_id) REFERENCES product(id));
  
DROP TABLE IF EXISTS loyality;
CREATE TABLE loyality (
	id SERIAL PRIMARY KEY,
	min_sum BIGINT UNSIGNED,
	max_sum BIGINT UNSIGNED,
	loyality FLOAT UNSIGNED);

DROP TABLE IF EXISTS stocks;
CREATE TABLE stocks (
	id SERIAL PRIMARY KEY,
	product_id BIGINT UNSIGNED,
	warehouse_id BIGINT UNSIGNED,
	stock INT DEFAULT 0,
	
	FOREIGN KEY (product_id) REFERENCES product(id),
	FOREIGN KEY (warehouse_id) REFERENCES warehouses(id));

